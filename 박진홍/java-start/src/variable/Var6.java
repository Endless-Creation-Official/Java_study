package variable;

public class Var6 {
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5;
        boolean c = true;
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열을 다루는 특별한 타입

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e.charAt(1));
    }
}
